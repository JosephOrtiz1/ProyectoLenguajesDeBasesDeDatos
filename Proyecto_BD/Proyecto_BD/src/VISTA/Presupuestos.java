    private void cargarDatosPresupuestos() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Presupuesto");
        modelo.addColumn("ID Usuario");
        modelo.addColumn("Monto Límite");
        modelo.addColumn("Fecha Inicio");
        modelo.addColumn("Fecha Fin");
        modelo.addColumn("Nombre del Presupuesto");
        modelo.addColumn("Tipo");

        tablaPresupuestos.setModel(modelo);

        try {
            Connection conn = (Connection) proyecto_bd.OracleConnection.conectar();
            if (conn == null) {
                JOptionPane.showMessageDialog(this, "No se pudo conectar a la base de datos.");
                return;
            }

            String sql = "SELECT id_presupuesto, id_usuario, monto_limite, fecha_inicio, fecha_fin, nombre_presupuesto, tipo FROM presupuestos";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                Object[] fila = new Object[7];
                fila[0] = rs.getInt("id_presupuesto");
                fila[1] = rs.getInt("id_usuario");
                fila[2] = rs.getDouble("monto_limite");
                fila[3] = rs.getDate("fecha_inicio");
                fila[4] = rs.getDate("fecha_fin");
                fila[5] = rs.getString("nombre_presupuesto");
                fila[6] = rs.getString("tipo");

                modelo.addRow(fila);
            }

            rs.close();
            pst.close();
            conn.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar los presupuestos: " + e.getMessage());
        }
    }

