    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        try {
            int idPresupuesto = Integer.parseInt(txtidPresupuesto1.getText());
            int idUsuario = Integer.parseInt(txtidUsuario1.getText());
            double montoLimite = Double.parseDouble(txtmontoLimite1.getText());
            String fechaInicio = fechaInicio1.getText();  
            String fechaFin = txtfechaFin1.getText();   
            String nombrePresupuesto = txtNombre1.getText();
            String tipo = txtTipo.getText();

            // Validar el formato de las fechas
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
            sdf.setLenient(false); // Validación estricta
            java.util.Date utilFechaInicio = sdf.parse(fechaInicio);
            java.util.Date utilFechaFin = sdf.parse(fechaFin);

            // Convertir java.util.Date a java.sql.Date
            java.sql.Date sqlFechaInicio = new java.sql.Date(utilFechaInicio.getTime());
            java.sql.Date sqlFechaFin = new java.sql.Date(utilFechaFin.getTime());

            // Conexión a la base de datos
            Connection conn = proyecto_bd.OracleConnection.conectar();
            if (conn == null) {
                JOptionPane.showMessageDialog(this, "No se pudo conectar a la base de datos.");
                return;
            }

            String sql = "INSERT INTO presupuestos (id_presupuesto, id_usuario, monto_limite, fecha_inicio, fecha_fin, nombre_presupuesto, tipo) VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, idPresupuesto);
            pst.setInt(2, idUsuario);
            pst.setDouble(3, montoLimite);
            pst.setDate(4, sqlFechaInicio);  // Fechas convertidas
            pst.setDate(5, sqlFechaFin);
            pst.setString(6, nombrePresupuesto);
            pst.setString(7, tipo);

            int filasAfectadas = pst.executeUpdate();
            if (filasAfectadas > 0) {
                JOptionPane.showMessageDialog(this, "Presupuesto agregado exitosamente.");
                cargarDatosPresupuestos();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo agregar el presupuesto.");
            }

            pst.close();
            conn.close();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese valores numéricos válidos para ID y Monto Límite.");
        } catch (java.text.ParseException e) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese fechas válidas en el formato dd-MM-yyyy.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al agregar el presupuesto: " + e.getMessage());
        }


    }//GEN-LAST:event_btnAgregarActionPerformed

